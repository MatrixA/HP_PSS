public void showOnTable( List<Customer> list){
        //1.获取制定表格（tblProduct）模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblcustomer.getModel();
        //2.清空表格信息
        while(dtm.getRowCount() > 0){
            dtm.removeRow(0);
        }
        //3.显示数据
        for(Customer p : list){
            Vector vt = new Vector();
            vt.add(p.getCusid());
            vt.add(p.getCusname()); 
            vt.add(p.getCustel());
            vt.add(p.getCusmobile());
            vt.add(p.getCusaddr());
           dtm.addRow(vt);
        }
}


