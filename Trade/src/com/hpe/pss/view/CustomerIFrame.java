    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        String cusname = this.txtcusname.getText().trim();
        String custel = this.txtcustel.getText().trim();
        String cusmoblie = this.txtMoblie.getText().trim();
        String cusaddr = this.txtcusaddr.getText().trim();
        if(StringUtil.checkLength(cusname) == false){
            JOptionPane.showMessageDialog(this, "类型为空");
            return;
        }
      
        if(StringUtil.checkLength(custel) == false){
            JOptionPane.showMessageDialog(this, "类型为空");
            return;
        }
        if(StringUtil.checkLength(cusaddr) == false){
            JOptionPane.showMessageDialog(this, "类型为空");
            return;
        }
        Customer p = new Customer(null,cusname,custel,cusmoblie,cusaddr);
        boolean result = pbiz.add(p);
        if(result == true){
            JOptionPane.showMessageDialog(this,"添加成功");
            List<Customer> list = pbiz.findAll();
            showOnTable(list);
        }else{
             JOptionPane.showMessageDialog(this,"添加失败");
        }
        //清空面板
        clearInput();
    }//GEN-LAST:event_btnaddActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        int answer = JOptionPane.showConfirmDialog(this,"您确定要删除吗？");
        if(answer == JOptionPane.YES_OPTION){
             //删除商品
             int cusid = Integer.parseInt(this.txtcusid.getText());
            boolean result = pbiz.delete(cusid);   
            if(result == true){
                 JOptionPane.showMessageDialog(this,"删除成功");
                 List<Customer> list = pbiz.findAll();
                showOnTable(list);
             }else{
            JOptionPane.showMessageDialog(this,"删除失败");
             }
        }else{
             JOptionPane.showMessageDialog(this,"取消删除");
        }
        
       
        //清空面板
        clearInput();
    }//GEN-LAST:event_btndeleteActionPerformed

