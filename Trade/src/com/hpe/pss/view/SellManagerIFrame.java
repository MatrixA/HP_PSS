public void initPurchaseTable(){
        JComboBox cobsup = new JComboBox();
        List<SellManager> suplist = pbiz.findAll();
        for(SellManager sup: suplist){
            cobsup.addItem(sup);       
 }
        this.tblsell.getColumnModel().getColumn(5).setCellEditor(new DefaultCellEditor(cobsup)); 

    }
