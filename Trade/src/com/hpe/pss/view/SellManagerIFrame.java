  private void addPurchaseTable(Vector vt) {
        boolean flag = false;
        DefaultTableModel dtm = (DefaultTableModel) this.tblsell.getModel();
        for(int i=0;i<dtm.getRowCount();i++){
            if(vt.get(0) == tblsell.getValueAt(i, 0)){
                flag = true;
                break;
            }
        }
        if(flag == false){
            dtm.addRow(vt);
        }
    }
    private void btnselloutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnselloutActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) this.tblsell.getModel();
        Object[][] sellManager = new Object[dtm.getRowCount()][5];//根据具体数值数创建采购数组
        Object[][] product = new Object[dtm.getRowCount()][2];//商品库存数组
        //遍历表格
        for(int i=0;i<dtm.getRowCount();i++){
         //sellManager[i][0] = NoUtil.createSelid();//采购单号
            sellManager[i][0] = this.tblsell.getValueAt(i, 0);//商品编号
            product[i][0] = this.tblsell.getValueAt(i, 2);
            sellManager[i][1] = this.tblsell.getValueAt(i, 1);//采购数量
            product[i][1] = this.tblsell.getValueAt(i, 0);
            sellManager[i][2] = this.tblsell.getValueAt(i, 3);//采购价格
            sellManager[i][3] = this.tblsell.getValueAt(i,4);
            sellManager[i][4] = ((SellManager)this.tblsell.getValueAt(i,5)).getCusname();
        }
        //调用批量添加
        boolean result = pbiz.sellout(sellManager, product);
        if(result == true){
            JOptionPane.showMessageDialog(this, "销售出库成功");
            //更新商品表
           // btnQueryActionPerformed(evt);//调用查询方法
        }else{
            JOptionPane.showMessageDialog(this, "销售出库失败");
        }
        //清空采购表
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
    }//GEN-LAST:event_btnselloutActionPerformed

