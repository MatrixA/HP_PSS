  private void  showOnTable (List<SellManager> list) {
        DefaultTableModel dtm = (DefaultTableModel) this.tblsell.getModel();
        //2.清空表中信息
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
            //3.显示数据
            for(SellManager p:list){
                Vector vt = new Vector();
                vt.add(p.getProid());
                vt.add(p.getProname());
           
                vt.add(p.getProcount());
                vt.add(p.getProprice());
                vt.add(p.getCusname());
            
                dtm.addRow(vt);
                
            }
    }
