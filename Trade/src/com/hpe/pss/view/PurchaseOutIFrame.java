    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        selectrow=this.tblPurchase.getSelectedRow();
        if(selectrow == -1){
            JOptionPane.showMessageDialog(this, "请选择要退货的采购单信息");
            return;
        }
        //获取退货信息
        Integer purid = Integer.parseInt(this.txtPurid.getText());
        Integer proid = Integer.parseInt(this.txtProid.getText());
        Integer purcount = Integer.parseInt(this.txtPurcount.getText());
        //获取退货数量
        String sreturnAmount = this.txtRetamount.getText();
        //数据验证
        String regex = "[1-9][0-9]*";
        if(sreturnAmount.matches(regex)== false){
            JOptionPane.showMessageDialog(this, "请填写整数");
            return;
        }
        //转换退货数量
        Integer retamount = Integer.parseInt(sreturnAmount);
        //判断是否超过采购数量
        if(retamount>purcount){
            JOptionPane.showMessageDialog(this, "退货数量大于采购数量");
            return;
        }
        String retreason=txtRetreason.getText();
        //调用业务
        java.sql.Date  rettime = new java.sql.Date( new java.util.Date().getTime());
        boolean result = 
                pbiz.purchasereturn(purid,retamount,rettime,proid,retreason);
        if(result == true){
            JOptionPane.showMessageDialog(this, "退货成功");
            btnQueryActionPerformed(evt);
        }else{
             JOptionPane.showMessageDialog(this, "退货失败");
        }
        //清空文本框
        this.txtProid.setText("");
        this.txtPurid.setText("");
        this.txtPurcount.setText("");
        this.txtRetamount.setText("");
        selectrow = -1;
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed


