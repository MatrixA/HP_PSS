    private void showOnTable(List<View_Purchase_detail> list) {
       //1 表格模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblPurchase.getModel();
        //2清空表格
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
        //3 添加数据
        for(View_Purchase_detail pur:list){
            Vector vt = new Vector();
            vt.add(pur.getPurid());
            vt.add(pur.getProid());
            vt.add(pur.getProname());
            vt.add(pur.getPurcount());
            vt.add(pur.getPurunitprice());
            vt.add(pur.getSupid());
            vt.add(pur.getSupname());
            vt.add(pur.getPurdate());
            dtm.addRow(vt);
            
        }
    }

