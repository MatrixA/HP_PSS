    private void showOnTable(List<ReturnProuct> list) {
        DefaultTableModel dtm = (DefaultTableModel) this.tblreturn.getModel();
        //2.清空表中信息
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
            //3.显示数据
            for(ReturnProuct p:list){
                Vector vt = new Vector();
                vt.add(p.getProid());
                vt.add(p.getProname());
                vt.add(p.getReturnamount());
                vt.add(p.getReturndate());
                vt.add(p.getCusname());
                vt.add(p.getRetid());
              
                dtm.addRow(vt);
                
            }
    }
