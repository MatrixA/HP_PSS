    private void btnreturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnreturnActionPerformed
        
        String Proid = this.txtproid.getText().trim();
        String proname = this.txtproname.getText().trim();
        String returnamount = this.txtreturnamount.getText().trim();
        String returnDate= this.txtreturndate.getText().trim();
        String cusname = this.txtcusname.getText().trim();
       //数据验证
        if(StringUtil.checkLength(Proid)==false){
            JOptionPane.showMessageDialog(this,"商品编号不能为空");
            return ;
        }
          String regex = "[1-9][0-9]*";
        if(returnamount.matches(regex)== false){
            JOptionPane.showMessageDialog(this, "请填写整数");
            return;
        }
        if(StringUtil.checkLength(returnamount)==false){
           JOptionPane.showMessageDialog(this,"销售数量不能为空");
           return ;
        }
        int proid = Integer.parseInt(this.txtproid.getText());
        int returnAmount = Integer.parseInt(this.txtreturnamount.getText());
        if(JOptionPane.showConfirmDialog(this, "确认退货")==2)return;
        else;
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd"); 
        
        java.sql.Date returndate = java.sql.Date.valueOf(returnDate);
        ReturnProuct p = new ReturnProuct(proid,proname,returnAmount,returndate,cusname,null);
        //调用业务类
        boolean result = pbiz.add(p);
        if(result == true){
            JOptionPane.showMessageDialog(this, "退货成功");
            List<ReturnProuct> list = pbiz.findAll();
           boolean update = pbiz.updateStoreCount(returnAmount,proid);
            
            showOnTable(list);
        }
        else{
             JOptionPane.showMessageDialog(this, "退货失败");
        }
        clearInput();
    }//GEN-LAST:event_btnreturnActionPerformed

