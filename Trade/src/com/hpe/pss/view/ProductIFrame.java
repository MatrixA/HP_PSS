    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        int answer = JOptionPane.showConfirmDialog(this,"您确定要删除吗？");
        if(answer == JOptionPane.YES_OPTION){
            //删除商品
            int supid = Integer.parseInt(this.txtproid.getText());
            boolean result = pbiz.delete(supid);
            System.out.println(result);
            if(result == true){
                JOptionPane.showMessageDialog(this,"删除成功");
                List<Product> list = pbiz.findAll();
                showOnTable(list);
            }else{
                JOptionPane.showMessageDialog(this,"删除失败");
            }
        }else{
            JOptionPane.showMessageDialog(this,"取消删除");
        }
        //清空面板
        clearInput();
    }//GEN-LAST:event_btndeleteActionPerformed

