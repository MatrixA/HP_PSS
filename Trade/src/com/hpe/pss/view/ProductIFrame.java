    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        String proname = this.txtproname.getText().trim();
        String type = this.txtprotype.getText().trim();
        String safe = this.txtsafe.getText().trim();
        String sugp = this.txtpropurchase.getText().trim();
        String sugs = this.txtprosell.getText().trim();
        String current = this.txtcurrent.getText().trim();
        String lsd = this.txtlastsell.getText().trim();
        String lpd = this.txtlastpurchase.getText().trim();
        Integer safeamount = Integer.parseInt(safe);
        Integer storecount = Integer.parseInt(current);
        BigDecimal sugpurchase = new BigDecimal(sugp);
        BigDecimal sugsell = new BigDecimal(sugs);
        java.sql.Date lastpurchase = java.sql.Date.valueOf(lpd);
        java.sql.Date lastsale = java.sql.Date.valueOf(lsd);
        //java.sql.Date lastpurchase = java.sql.Date.valueOf(lastpurchase_s);
        Product p = new Product(null,proname,type,safeamount,sugpurchase,sugsell,storecount,lastpurchase,lastsale);
        boolean result = pbiz.add(p);
        if(result == true){
            JOptionPane.showMessageDialog(this,"添加成功");
            List<Product> list = pbiz.findAll();
            showOnTable(list);
        }else{
            JOptionPane.showMessageDialog(this,"添加失败");
        }
        //清空面板
        clearInput();

    }//GEN-LAST:event_btnaddActionPerformed

