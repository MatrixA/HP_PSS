    public void showOnTable( List<Product> list){
        //1.获取制定表格（tblProduct）模型
        DefaultTableModel dtm = (DefaultTableModel) this.tblproduct.getModel();
        //2.清空表格信息
        while(dtm.getRowCount() > 0){
            dtm.removeRow(0);
        }
        //3.显示数据
        for(Product p : list){
            Vector vt = new Vector();
            vt.add(p.getProid());
            vt.add(p.getProname()); 
            vt.add(p.getType());
            vt.add(p.getSafeamount());
            vt.add(p.getSugpurchase());
            vt.add(p.getSugsell());
            vt.add(p.getStorecount());
            vt.add(p.getLastpurchase());
            vt.add(p.getLastsale());
           dtm.addRow(vt);
        }
}
