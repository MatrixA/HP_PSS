    private void showOnTable(List<Product> list) {
        DefaultTableModel dtm=
                (DefaultTableModel)this.tblProduct.getModel();
        //2.清空表格信息
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
        //3.显示数据表格
        for(Product p : list){
            Vector vt=new Vector();
            vt.add(p.getProid());
            vt.add(p.getProname());
            vt.add(p.getType());
            vt.add(p.getStorecount());
            vt.add(p.getSugpurchase());
            vt.add(p.getSugsell());
            dtm.addRow(vt);
        }
    }

