    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) this.tblPurchase.getModel();
        Object[][] purchase = new Object[dtm.getRowCount()][5];//根据具体数值数创建采购数组
        Object[][] stocks = new Object[dtm.getRowCount()][2];//商品库存数组
        //遍历表格
        for(int i=0;i<dtm.getRowCount();i++){
            //采购表
            //0编号
            purchase[i][0] = this.tblPurchase.getValueAt(i, 0);//商品编号
            //购买日期
            purchase[i][1] = new java.util.Date();
            //采购数量(验证) 
            String count_s = (String) this.tblPurchase.getValueAt(i, 4);
            if(StringUtil.checkDigit(count_s)){
            purchase[i][2] = new Integer(count_s); //采购数量
            }else{
               JOptionPane.showMessageDialog(this, "数量必须为数字！");
                return;
            }
           
            //采购价格（验证）
            String price_s = (String) this.tblPurchase.getValueAt(i, 5);
            if(StringUtil.checkDecimal(price_s)){
            purchase[i][3] = new BigDecimal(price_s) ;//采购价格
            }else{
                JOptionPane.showMessageDialog(this, "价格必须为数字！");
                return;
            }
            //供应商
            Supplier sup = (Supplier)this.tblPurchase.getValueAt(i, 6);
            purchase[i][4] = sup.getSupid();
            //商品表所需
            stocks[i][1] = this.tblPurchase.getValueAt(i, 0);
            //数量（验证）
            stocks[i][0] = this.tblPurchase.getValueAt(i, 4);
        }
        //调用批量添加
        boolean result = purbiz.purchasein(purchase, stocks);
        if(result == true){
            JOptionPane.showMessageDialog(this, "采购入库成功");
            //更新商品表
            btnSearchActionPerformed(evt);//调用查询方法
        }else{
            JOptionPane.showMessageDialog(this, "采购入库失败");
        }
        //清空采购表
        while(dtm.getRowCount()>0){
            dtm.removeRow(0);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed


